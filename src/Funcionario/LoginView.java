    private void valida()
    {
        ArrayList<Funcionario> fu = fuDal.searchLogin(txtLogin.getText().trim(), EncriptaSenha.encripta(txtPassword.getText().trim()));
        if (!fu.isEmpty())
        {
            String args[] = new String[1];
            args[0] = fu.get(0).getNome();
            JOptionPane.showMessageDialog(null, "Olá " + fu.get(0).getNome() + " Bem vindo ao sistema GestSport!");
            Menu.main(args);
            dispose();
        } else
        {
            JOptionPane.showMessageDialog(null, "Utilizador ou password incorretos");
            txtPassword.requestFocus();
        }
    }
