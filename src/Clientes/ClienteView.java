    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnGravarActionPerformed
    {//GEN-HEADEREND:event_btnGravarActionPerformed
        if (validaCampos() == false)
        {
            Cliente c = new Cliente();

            c.setNome(txtNome.getText());
            c.setMorada(txtMorada.getText());
            c.setEmail(txtEmail.getText());
            c.setNif(txtNif.getText());
            c.setCodPostal(txtCpostal.getText());
            c.setTelefone(txtTelefone.getText());
            c.setUrl(txtWWW.getText());


            if (clDal.Insert(c))
            {
                JOptionPane.showMessageDialog(rootPane, "Adicionado com sucesso!");
            }
            eNovo = false;
            mudaEstado(false);
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEliminarActionPerformed
    {//GEN-HEADEREND:event_btnEliminarActionPerformed
        int resposta = JOptionPane.showConfirmDialog(rootPane,
                "Pretende remover o instrumento selecionado?",
                "Remover", JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION)
        {
            int clCod = Integer.parseInt(txtCod.toString());
            if (clDal.Delete(clCod))
            {
                JOptionPane.showMessageDialog(rootPane, "Cliente removido!");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

