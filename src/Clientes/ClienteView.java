    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnNovoActionPerformed
    {//GEN-HEADEREND:event_btnNovoActionPerformed
        int ultCli = 1;
        try
        {
            ultCli = clDal.ultimoCli() + 1;
        } catch (SQLException ex)
        {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }

        txtCod.setText(String.valueOf(ultCli));
        txtNome.setText("");
        txtEmail.setText("");
        txtMorada.setText("");
        txtNif.setText("");
        txtTelefone.setText("");
        txtWWW.setText("");
        txtCidade.setText("");
        txtCpostal.setText("");
        btnGravar.setEnabled(true);
        btnCancelar.setEnabled(true);

        mudaEstado(true);
        txtNome.requestFocus();
        eNovo = true;
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnRightActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnRightActionPerformed
    {//GEN-HEADEREND:event_btnRightActionPerformed
        int cod = Integer.parseInt(txtCod.getText().trim());

        cl = clDal.searchByCod(cod, 0);

        if (cl.size() == 1)
        {
            for (Cliente cx : cl)
            {
                //System.out.println(cx.getCodcliente() + " - " + cx.getNome());
                txtCod.setText(String.valueOf(cx.getCodcliente()));
                txtNome.setText(cx.getNome());
                txtEmail.setText(cx.getEmail());
                txtMorada.setText(cx.getMorada());
                txtCpostal.setText(cx.getCodPostal());
                mostraCidade(txtCpostal.getText());
                txtNif.setText(cx.getNif());
                txtTelefone.setText(cx.getTelefone());
                txtWWW.setText(cx.getUrl());
            }
        } else
        {
            JOptionPane.showMessageDialog(rootPane, "Ultimo cliente!");
        }
    }//GEN-LAST:event_btnRightActionPerformed

