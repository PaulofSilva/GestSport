    private boolean validaCampos()
    {
        boolean falta = false;

        if (txtCpostal.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Preencha o Codigo Postal");
            txtCpostal.requestFocus();
            falta = true;
        } else if (txtNif.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Preencha o NIF");
            txtNif.requestFocus();
            falta = true;
        }
        return falta;
    }

